          p To answer this question is to take the passage as a whole and attempt to summarize it in as brief and simple a way as possible. Scholars such as R.C. Sproul have said that if a teacher can’t explain a text in terms a child would understand then they still don’t understand it well themselves. “What’s the Big Idea?” asks what is the main point or thrust of the text for the original hearers or readers. Answering this question is at once simple and profound; all it attempts to do is understand the basic message of the text. But in doing so, we (the readers) must distinguish between points, words, terms and ideas that are more central to the meaning from those which are less central. This leads us to the next question.

